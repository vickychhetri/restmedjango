from django.shortcuts import render
from rest_framework.response import Response
from rest_framework import status, generics
from .models import Task
from .serializers import TaskSerializer
import math

from datetime import datetime


# Create your views here.
class Tasks(generics.GenericAPIView):
    serializer_class = TaskSerializer
    queryset = Task.objects.all()

    def post(self, request):
        serializer = self.serializer_class(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response({"status": "success", "task": serializer.data}, status=status.HTTP_201_CREATED)
        else:
            return Response({"status": "fail", "message": serializer.errors}, status=status.HTTP_400_BAD_REQUEST)

    def get(self, request):
        page_num = int(request.GET.get("page", 1))
        limit_num = int(request.GET.get("limit", 10))
        start_num = (page_num - 1) * limit_num
        end_num = limit_num * page_num
        search_param = request.GET.get("search")
        tasks = Task.objects.all()
        total_notes = tasks.count()
        if search_param:
            notes = tasks.filter(title__icontains=search_param)
        serializer = self.serializer_class(notes[start_num:end_num], many=True)
        return Response({
            "status": "success",
            "total": total_notes,
            "page": page_num,
            "last_page": math.ceil(total_notes / limit_num),
            "tasks": serializer.data
        })